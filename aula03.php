<p>Brincadeira, gafe ou crime? Condenado recentemente a pagar R$ 50 mil por danos morais à população negra e, agora, denunciado ao Supremo Tribunal (STF) pelo crime de racismo, o pré-candidato a presidente e deputado federal Jair Bolsonaro (PSL/RJ) trata a fala sobre os quilombolas que lhe rendeu os processos como “brincadeira”. Desta vez, ele disse em uma palestra que um negro não servia nem para procriar.</p>
