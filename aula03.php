      <h2>Base de Bolsonaro inunda internet com memes contra o Congresso e o STF</h2>
<img src="https://pbs.twimg.com/media/ERs_zIwW4AIXF27?format=jpg&name=small" alt="Trulli" width="500" height="333">
<p>No discurso oficial, presidente diz que confronto com Congresso e Supremo é "ilação". Na prática, seus apoiadores atacam abertamente Maia, Alcolumbre e Toffoli. Tem um até que prega fechamento dos Poderes.</p>
<p>Na noite de terça-feira (25), a jornalista Vera Magalhães, do Estadão, revelou que o presidente Jair Bolsonaro compartilhou vídeos endossando manifestações em 15 de março em apoio a seu governo e contra instituições como o Congresso e o STF (Supremo Tribunal Federal).</p>
<p>Na manhã desta quarta-feira (26), Bolsonaro foi às redes sociais dizer que que troca mensagens era de cunho pessoal com "amigos" no WhatsApp e que "qualquer ilação fora desse contexto são tentativas rasteiras de tumultuar a República". O presidente não negou o envio dos vídeos</p>

